# example of arthematic operator 
a = 10
b = 5

# addition 
add =  a + b

# subtract 
sub = a - b

# divide 
div = a / b

# multiplication 
mult =  a * b

# floor division 
floordiv = a // b

# modulo 
mode =  a % b

# power / exponential 
expo =  a ** b

# outputs 
print(add)
print(sub)
print(div)
print(mult)
print(floordiv)
print(mode)
print(expo)

